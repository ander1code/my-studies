// Operadores:

var x = 10;
var y = 3;

console.log(`${x} + ${y} = ${x + y}`);
console.log(`${x} - ${y} = ${x - y}`);
console.log(`${x} x ${y} = ${x * y}`);
console.log(`${x} / ${y} = ${x / y}`);
console.log(`${x} % ${y} = ${x % y}`);
console.log(`${x} ** ${y} = ${x ** y}`); //10 x 10 x 10 = 1000
console.log(`++${x} = ${++x}`);
console.log(`${y}++ = ${y++}`);
console.log(`-${x} = ${-x}`);
