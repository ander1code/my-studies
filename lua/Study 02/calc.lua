local calc = {}

function calc.somar(num1, num2)
    return num1 + num2
end

function calc.subtrair(num1, num2)
    return num1 - num2
end

function calc.multiplicar(num1, num2)
    return num1 * num2
end

function calc.dividir(num1, num2)
    return num1 / num2
end

function calc.modular(num1, num2)
    return num1 % num2
end

return calc